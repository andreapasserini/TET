# Install libguava.jar
wget http://search.maven.org/remotecontent?filepath=com/google/guava/guava/19.0/guava-19.0.jar
mv remotecontent*guava-19.0.jar lib/guava.jar

# Install MySQL-Java connector
wget http://central.maven.org/maven2/mysql/mysql-connector-java/5.0.8/mysql-connector-java-5.0.8.jar
mv mysql-connector-java-5.0.8.jar lib/mysql.jar



